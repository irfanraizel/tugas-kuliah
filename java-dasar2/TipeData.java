public class TipeData {
    public static void main(String[] args) {
        //tipe data di java:
        //byte, short, integer, long, float, duoble, char, boolean
        
        //byte
        byte varbyte = 10;
        System.out.println("========BYTE========");
        System.out.println("Nilai varbyte adalah =" + varbyte);
        System.out.println("Nilai max byte adalah =" + Byte.MAX_VALUE);
        System.out.println("Nilai min byte adalah =" + Byte.MIN_VALUE);
        System.out.println("Size byte adalah =" + Byte.BYTES+"bytes");
        System.out.println("Size byte adalah =" + Byte.SIZE+"bit");
        
        //short
        short varshort = 10;
        System.out.println("========SHORT========");
        System.out.println("Nilai varshort adalah =" + varshort);
        System.out.println("Nilai max short adalah =" + Short.MAX_VALUE);
        System.out.println("Nilai min short adalah =" + Short.MIN_VALUE);
        System.out.println("Size short adalah =" + Short.BYTES+"bytes");
        System.out.println("Size short adalah =" + Short.SIZE+"bit");
        
        //int
        int varint = 10;
        System.out.println("========INTEGER========");
        System.out.println("Nilai varint adalah =" + varint);
        System.out.println("Nilai max int adalah =" + Integer.MAX_VALUE);
        System.out.println("Nilai min int adalah =" + Integer.MIN_VALUE);
        System.out.println("Size int adalah =" + Integer.BYTES+"bytes");
        System.out.println("Size int adalah =" + Integer.SIZE+"bit");
        
        //long
        long varlong = 10l;
        System.out.println("========LONG========");
        System.out.println("Nilai varlong adalah =" + varlong);
        System.out.println("Nilai max long adalah =" + Long.MAX_VALUE);
        System.out.println("Nilai min long adalah =" + Long.MIN_VALUE);
        System.out.println("Size long adalah =" + Long.BYTES+"bytes");
        System.out.println("Size long adalah =" + Long.SIZE+"bit");
        
        //float
        float varfloat = 8.5f;
        System.out.println("========Float========");
        System.out.println("Nilai varfloat adalah =" + varfloat);
        System.out.println("Nilai max float adalah =" + Float.MAX_VALUE);
        System.out.println("Nilai min float adalah =" + Float.MIN_VALUE);
        System.out.println("Size float adalah =" + Float.BYTES+"bytes");
        System.out.println("Size float adalah =" + Float.SIZE+"bit");
        
        //double
        double vardouble = 8.5d;
        System.out.println("========Double========");
        System.out.println("Nilai vardoublet adalah =" + vardouble);
        System.out.println("Nilai max double adalah =" + Double.MAX_VALUE);
        System.out.println("Nilai min double adalah =" + Double.MIN_VALUE);
        System.out.println("Size double adalah =" + Double.BYTES+"bytes");
        System.out.println("Size double adalah =" + Double.SIZE+"bit");
        
        //char
        char c = 'c';
        System.out.println("========Char========");
        System.out.println("Nilai c adalah =" + c);
        System.out.println("Nilai max char adalah =" + Character.MAX_VALUE);
        System.out.println("Nilai min char adalah =" + Character.MIN_VALUE);
        System.out.println("Size char adalah =" + Character.BYTES+"bytes");
        System.out.println("Size char adalah =" + Character.SIZE+"bit");
        
        //boolean
        boolean d = true;
        System.out.println("========Boolean========");
        System.out.println("Nilai d adalah =" + d);
    }
}
